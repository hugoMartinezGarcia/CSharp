// Números reales: valor inicial de un float y de un double

using System;
class Ejercicio_03_02_02_01
{
    static void Main()
    {
        int i1 = 13, i2 = 6;
        float f1 = 13, f2 = 6;
        double d1 = 13, d2 = 6;
        
        Console.WriteLine("13/6 de enteros = {0}", i1 / i2);
        Console.WriteLine("13/6 de simple precisión = {0}", f1 / f2);
        Console.WriteLine("13/6 de doble precisión = {0}", d1 / d2);
    
    }
}
